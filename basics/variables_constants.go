package main

import "fmt"

func main() {
	// declearing a variable var <variable_name> <data_type> = <value>(optional)
	var x int = 10
	var y float32 = 43.5
	var i, j, f = 11, 13.6, "go"
	var ran string = "golang"

	fmt.Println("x:", x)
	fmt.Println("y:", y)
	fmt.Println("i, j, f:", i, j, f)
	fmt.Println("ran:", ran)
}
